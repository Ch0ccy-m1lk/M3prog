<?php
// Fahrenheit to Celsius

$Fahrenheit = 74;
$celsius = ($Fahrenheit - 32) / 1.8;

echo "{$Fahrenheit} degrees Fahrenheit = {$celsius} degrees celsius.   <br>";

$celsius2 = 74;
$fahrenheit2 = ($celsius2 *1.8) + 32;

echo "{$celsius2} degrees celsius = {$fahrenheit2} degrees fahrenheit. <br>";


?>